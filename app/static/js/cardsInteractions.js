document.addEventListener('alpine:init', () => {
    Alpine.data('cardsInteractions', (currentTab, people, columns, keyColumn) => ({
        // #region initializations
        people: people,
        workersState: null,
        rawColumns: columns,
        fields: Object.entries(columns).map(([key, value]) => ({ [key]: value })),
        key_column: keyColumn,

        init() {
            this.$store.workersState.currentTab = currentTab;
            this.$store.workersState.initializeCards(people);
            this.$store.workersState.columns = this.rawColumns;
            this.$store.workersState.fields = this.fields;
            this.$store.workersState.keyColumn = keyColumn;
            this.$store.currentFilters = {
                searchColumn: null,
                searchValue: null,
            },

            this.workersState = this.$store.workersState.globalState;

            this.$watch('Alpine.store("workersState").globalState', (newState) => {
                this.workersState = newState;

                console.log('Global state changed to:', this.workersState);
            });
        },
        // #endregion

        // #region row creation
        createNewCard() {
            
            const cardGrid = document.getElementById("card-grid");
            const card = document.createElement("div"); 
            card.className = `flex flex-col w-full bg-white border border-gray-200 rounded-lg shadow dark:bg-gray-800 dark:border-gray-700 items-center`; 
            card.id = 'card-creation-form';

            const fieldsJson = JSON.stringify(this.fields);
            card.setAttribute('x-data', `createCard(${fieldsJson})`);

            
            let innerHTML = `
                <div class="w-full flex px-4 pt-4 justify-end">
                    <p class="text-gray-500 dark:text-gray-400">Creating...</p>
                </div>
                <div class="w-full flex flex-col items-center px-10 pb-10" >
                    <img class="w-24 h-24 mb-3 rounded-full shadow-lg" src="https://i.ibb.co/f93t1mB/user.png" alt="person image"/>
                    <div class="w-full">
                        <ul class="mt-3 h-40 overflow-y-scroll divide-y bg-white py-2 px-3 text-gray-600
                        hover:text-gray-700 dark:bg-gray-800 dark:divide-gray-500 dark:text-gray-200 
                        custom-scrollbar">`;

            this.fields.forEach(fieldObject => {
                const fieldName = Object.keys(fieldObject)[0]; 
                innerHTML += `
                <li class="flex items-center py-3 text-sm">
                    <span class="font-bold">${fieldName}</span>
                `;
                if (fieldObject[fieldName] === 'PK') {
                    innerHTML += `<span class="ml-auto text-gray-500 dark:text-gray-400">${fieldName} is autogenerated</span>`;
                } else if (fieldObject[fieldName] === 'ATTRIB') {
                    if (fieldName.toLowerCase().includes('date')) {
                        innerHTML += `
                        <div class="ml-auto w-1/2 relative max-w-sm">
                            <div class="absolute inset-y-0 start-0 flex items-center ps-3 pointer-events-none">
                            <svg class="w-4 h-4 text-gray-500 dark:text-gray-400" aria-hidden="true" xmlns="http://www.w3.org/2000/svg" fill="currentColor" viewBox="0 0 20 20">
                                <path d="M20 4a2 2 0 0 0-2-2h-2V1a1 1 0 0 0-2 0v1h-3V1a1 1 0 0 0-2 0v1H6V1a1 1 0 0 0-2 0v1H2a2 2 0 0 0-2 2v2h20V4ZM0 18a2 2 0 0 0 2 2h16a2 2 0 0 0 2-2V8H0v10Zm5-8h10a1 1 0 0 1 0 2H5a1 1 0 0 1 0-2Z"/>
                                </svg>
                            </div>
                            <input datepicker datepicker-autohide type="text" 
                            id="${fieldName}
                            class="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg 
                            focus:ring-blue-500 focus:border-blue-500 block w-full ps-10 p-2.5  dark:bg-gray-700 
                            dark:border-gray-600 dark:placeholder-gray-400 dark:text-white dark:focus:ring-blue-500 
                            dark:focus:border-blue-500" placeholder="Select date">
                        </div>`;
                    } else {
                        innerHTML += `<input type="text" id="${fieldName}"
                                    class="bg-gray-50 border border-gray-300 text-gray-900 
                                    text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 
                                    ml-auto w-1/2 p-2.5 dark:bg-gray-700 dark:border-gray-600 
                                    dark:placeholder-gray-400 dark:text-white 
                                    dark:focus:ring-cyan-500 dark:focus:border-cyan-500" 
                                    placeholder="${fieldName}"/>`;
                    }
                }
                innerHTML += `</li>`;
            });

            innerHTML += `</ul>
                        </div>
                    </div>
                    <div class="w-2/3 flex px-4 pb-4 justify-between">
                        <button @click="cancelCreatingCard()"
                        class="text-gray-900 gap-2 px-5 py-2.5 me-2 mb-4 focus:outline-none bg-white font-medium  rounded-lg text-sm border
                        border-gray-200 hover:bg-gray-100 hover:text-cyan-700 focus:z-10 focus:ring-4 focus:ring-gray-100 
                        dark:focus:ring-gray-700 dark:bg-gray-800 dark:text-gray-400 dark:border-gray-600 dark:hover:text-white
                        dark:hover:bg-gray-700">
                            Cancel
                            <i class="fa-solid fa-xmark"></i>
                        </button>
                        <button @click="saveCreatedCard()"
                        class="text-white gap-2 bg-cyan-600 hover:bg-sky-600 
                        focus:ring-4 focus:ring-blue-300 font-medium rounded-lg text-sm 
                        px-5 py-2.5 me-2 mb-4 dark:bg-cyan-800 dark:hover:bg-cyan-900 
                        focus:outline-none dark:focus:ring-cyan-950">
                            Save
                            <i class="fa-solid fa-check"></i>
                        </button>
                    </div>`;

            console.log(innerHTML);

            card.innerHTML = innerHTML;

            const firstChild = document.getElementById("card-header").nextSibling;
            cardGrid.insertBefore(card, firstChild);
        },
        // #endregion

    }));
});